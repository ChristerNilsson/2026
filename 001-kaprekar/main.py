# Starta med ett fyrsiffrigt tal.
# Använd minst två olika siffror.
# Sortera siffrorna fallande => a
# Sortera siffrorna stigande => b
# Beräkna a-b
# Loopa tills 6174 uppnås.
# Det ska ta högst sju gånger.

count = [0] * 8

def kaprekar(n):
	if n == 0: return "0000"
	if n == 6174: return "6174"

	s = str(n)                      # "123"
	s = s.zfill(4)                  # "0123"

	a = list(s)                     # ["0","1","2","3"]
	a.sort()                        # ["3","2","1","0"]
	b = int("".join(a))             # 3210
	a.reverse()                     # ["0","1","2","3"]
	a = int("".join(a))             # 123

	return s + " " + kaprekar(a-b)  # "0123 3087 8352 6174

for i in range(1,10000):
	s = kaprekar(i)
	n = (len(s) + 1)//5 - 1
	count[n] += 1
	print(n,s)

print()
for i in range(8):
	print(i,count[i])

